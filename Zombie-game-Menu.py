def Instructions():
    gf.clear_screen()
    instructions_text = """
        Instructions:
        
        1. Objective:
           Survive the zombie apocalypse by making strategic decisions,
           managing resources, and combating zombie threats.
        
        2. Controls:
           - Use the keyboard to navigate menus and make selections.
           - Follow on-screen prompts for actions during gameplay.
        
        3. Gameplay Tips:
           - Explore your surroundings thoroughly to find supplies.
           - Conserve ammunition and use melee attacks when possible.
           - Keep an eye on your health and stamina levels.
           - Make alliances with other survivors when possible.
        
        4. Saving and Loading:
           - You can save your progress at designated safe zones.
           - Load previous saves from the main menu to continue your adventure."""
    gf.typewriter_effect(instructions_text, delay=0.02)

def exit_game():
    gf.clear_screen()
    gf.typewriter_effect("Exiting the game... Goodbye!", delay=0.05)
    time.sleep(1)
    gf.clear_screen()
    exit()

